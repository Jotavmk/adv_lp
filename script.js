// Background carousel functionality
function initBackgroundCarousel() {
    const slides = document.querySelectorAll('.hero-bg-slide');
    let currentSlide = 0;

    console.log('Carousel slides found:', slides.length);

    function showSlide(index) {
        console.log('Showing slide:', index);
        slides.forEach((slide, i) => {
            slide.classList.toggle('active', i === index);
        });
    }

    function nextSlide() {
        currentSlide = (currentSlide + 1) % slides.length;
        showSlide(currentSlide);
    }

    // Auto-play every 4 seconds
    setInterval(nextSlide, 4000);
}

// Initialize background carousel when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    console.log('DOM loaded, initializing carousel...');
    initBackgroundCarousel();
});

// Also initialize when window loads (fallback)
window.addEventListener('load', function() {
    console.log('Window loaded, checking carousel...');
    const slides = document.querySelectorAll('.hero-bg-slide');
    if (slides.length > 0) {
        console.log('Slides found on window load:', slides.length);
    }
}); 